#include "philo.h"

int	check_args(int ac, char **av)
{
	int i;

	i = -1;
	if(ac < 4 || ac > 5)
		return (0);
	while(av[++i])
	{
		if(!ft_atoi(av[i]))
			return (0);
	}
	return (1);
}